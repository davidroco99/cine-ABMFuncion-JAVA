    public void cargarTabla() {
          
        DefaultTableModel tabla = new DefaultTableModel(){
            
            //filas y columnas no editables
            @Override
            public boolean isCellEditable (int row, int column){
                return false;
            }
        };
        //establecer nombre y columnas
        
        String titulos[] = {"NRO-Película", "Título", "Director", "Elenco", "Duración", "Género", "Fecha Alta", "Fecha Baja"};
        tabla.setColumnIdentifiers(titulos);
        
        
        //carga de los datos desde la base de datos
        List <DTOPelicula> listaPeliculas = controla.traerPeliculas();
        
        
        //recorrer la lista y mostrar cada uno de llos elementos en la tabla
        
        if (listaPeliculas != null){
            for (DTOPelicula dtoPelicula : listaPeliculas){
                if (dtoPelicula.getFechaBaja() == null){
                   Object[] objeto = {dtoPelicula.getIdPelicula(), dtoPelicula.getTitulo(),dtoPelicula.getDirector(), dtoPelicula.getElenco(), dtoPelicula.getDuracion(), dtoPelicula.getGenero(), new SimpleDateFormat("dd-MM-yyyy").format(dtoPelicula.getFechaAlta()), dtoPelicula.getFechaBaja()}; 
                   tabla.addRow(objeto);
                }else{
                   Object[] objeto1 = {dtoPelicula.getIdPelicula(), dtoPelicula.getTitulo(),dtoPelicula.getDirector(), dtoPelicula.getElenco(), dtoPelicula.getDuracion(), dtoPelicula.getGenero(), new SimpleDateFormat("dd-MM-yyyy").format(dtoPelicula.getFechaAlta()), new SimpleDateFormat("dd-MM-yyyy").format(dtoPelicula.getFechaBaja())};
                   tabla.addRow(objeto1);
                }
                
            }
        }
        tablaPelicula.setModel(tabla);

    }
    
    
    public void mostrarMensajes(String mensaje, String tipo,String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            
        }
        else if (tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);  
    }
