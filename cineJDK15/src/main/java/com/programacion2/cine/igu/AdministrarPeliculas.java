    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //hay mas de una fila en la tabla
        if (tablaPelicula.getRowCount() > 0) {
            //hay por lo menos una selección
            if (tablaPelicula.getSelectedRow() != -1) {
                //optengo la id a borrar
                int idPelicula = Integer.parseInt(String.valueOf(tablaPelicula.getValueAt(tablaPelicula.getSelectedRow(), 0)));
                
                controla.borrarPelicula(idPelicula);
                
                mostrarMensajes("Se Eliminó correctamente La Pelicula","Info", "Eliminación Exitosa");
            }
            else{
                mostrarMensajes("No Seleccionó ningún Registro","Error", "No se elimnó el registro");
            }
        }
        else{
            mostrarMensajes("No hay registros para eliminar","Error", "No se elimnó el registro");
        }
        cargarTabla();                                          
        
    }//GEN-LAST:event_btnEliminarActionPerformed

